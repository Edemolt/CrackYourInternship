class Solution
{
  public:
    char firstRep (string s)
    {
        //code here.
        vector<int> v(26,0);
        for(char &ch: s){
            v[ch - 'a'] += 1;
        }
        
        for(char ch: s){
            if(v[ch - 'a'] > 1) return ch;
        }
        return '#';
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string s; cin >> s;
        Solution ob;
        char res = ob.firstRep (s);
        if (res == '#') cout << -1 << endl;
        else cout << res << endl;
    }
    return 0;
}



